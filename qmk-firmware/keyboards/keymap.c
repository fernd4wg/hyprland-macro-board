bool oled_task_user(void) {
  switch (get_highest_layer(layer_state)) {
        case _HYPRLAND:
            hypr_oled();
            break;
        case _VIM:
            vim_oled();
            break;
        case _GENERAL:
            general_oled();
            break;
        case _FN:
            fn_oled();
            break;
        case _NUMPAD:
            numpad_oled();
            break;
        default:
            // call function to display idle screen
      return false;
    }
}
